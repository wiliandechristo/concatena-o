// Usando o operador de concatenação '+'
let string1 = "Olá";
let string2 = "mundo";
let resultado1 = string1 + ", " + string2;
console.log(resultado1); // Saída: Olá, mundo

// Usando o método concat()
let resultado2 = string1.concat(", ", string2);
console.log(resultado2); // Saída: Olá, mundo
